
public class Principal {

	public static void main(String[] args) {

		System.out.println("Valor absoluto :" + Math.abs(-5.69874d));
		System.out.println("Valor absoluto :" + Math.abs(-6.3654f));
		System.out.println("Valor absoluto :" + Math.abs(-6));
		System.out.println("Valor absoluto :" + Math.abs(-10000000000000L));
		System.out.println("Arco coseno :" + Math.acos(0.369d));
		System.out.println("Suma :" + Math.addExact(5, 12));
		System.out.println("Suma :" + Math.addExact(12L, 36L));
		System.out.println("Arco seno :" + Math.asin(0.369d));
		System.out.println("Arco tangente :" + Math.atan(5.69874d));
		System.out.println("Raíz cúbica :" + Math.cbrt(5.69874d));
		System.out.println("Coseno trigonométrico de un ángulo :" + Math.cos(5.69874d));
		System.out.println("Número e de Euler elevado a la potencia :" + Math.exp(5.69874d));
		System.out.println("Logaritmo natural (base e )); :" + Math.log(5.69874d));
		System.out.println("Logaritmo de base 10 :" + Math.log10(5.69874d));
		System.out.println("Mayor :" + Math.max(5.69874d, 96355.36565d));
		System.out.println("Mayor :" + Math.max(6.3654f, 1.698f));
		System.out.println("Mayor :" + Math.max(6, 2));
		System.out.println("Mayor :" + Math.max(10000000000000L, 600000000L));
		System.out.println("Menor :" + Math.min(5.69874d, 96355.36565d));
		System.out.println("Menor :" + Math.min(6.3654f, 1.698f));
		System.out.println("Menor :" + Math.min(6, 2));
		System.out.println("Menor :" + Math.min(10000000000000L, 600000000L));
		System.out.println("Producto :" + Math.multiplyExact(5, 12));
		System.out.println("Producto :" + Math.multiplyExact(12L, 36L));
		System.out.println("Valor del primer argumento elevado a la potencia del segundo argumento :"
				+ Math.pow(5.69874d, 3.36d));
		System.out
				.println("Double valor con un signo positivo, mayor o igual que 0.0 y menor que 1.0 :" + Math.random());
		System.out.println("Long redondeado más cercano :" + Math.round(5.69874d));
		System.out.println("Int mas cercano y redondeado a :" + Math.round(6.3654f));
		System.out.println("Seno trigonométrico de un ángulo :" + Math.sin(5.69874d));
		System.out.println("Raíz cuadrada positiva correctamente redondeada :" + Math.sqrt(5.69874d));
		System.out.println("Tangente trigonométrica de un ángulo :" + Math.tan(5.69874d));

	}

}